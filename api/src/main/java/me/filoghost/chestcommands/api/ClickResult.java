package me.filoghost.chestcommands.api;

/**
 * Enum of possible actions applied to a menu view after a player clicks on an icon.
 *
 * @since 1
 */
public enum ClickResult {

    /**
     * Keeps the menu view open.
     *
     * @since 1
     */
    KEEP_OPEN,

    /**
     * Closes the menu view.
     *
     * @since 1
     */
    CLOSE

}
